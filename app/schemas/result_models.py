class S5Result(BaseModel):
    """
    Model for S5 strategy generation output.

    This model represents the structured strategy advice generated by LLM
    based on S2, S3, and R1 inputs, after guardrail corrections.
    """
    psychological_analysis: str = Field(
        default="",
        description="Psychological analysis based on signals (300 characters max)"
    )
    risk_points: List[str] = Field(
        default_factory=lambda: ["信号有限，难以准确判断"],
        description="Identified risk points in the current relationship"
    )
    suggestions: List[str] = Field(
        default_factory=lambda: ["保持观察", "避免重大决定"],
        description="Actionable suggestions appropriate for current stage"
    )
    next_step: str = Field(
        default="继续观察互动模式",
        description="Clear next step action recommendation"
    )

    @validator('psychological_analysis')
    def truncate_psychological_analysis(cls, v):
        """Truncate psychological analysis if too long."""
        if len(v) > 300:
            return v[:297] + "..."
        return v

    @validator('next_step')
    def truncate_next_step(cls, v):
        """Truncate next step if too long."""
        if len(v) > 100:
            return v[:97] + "..."
        return v

    @validator('risk_points')
    def ensure_min_risk_points(cls, v):
        """Ensure at least one risk point."""
        if not v:
            return ["信号有限，难以准确判断"]
        return v

    @validator('suggestions')
    def ensure_min_suggestions(cls, v):
        """Ensure at least two suggestions."""
        if len(v) < 2:
            return v + ["保持适度互动频率"] if v else ["保持观察", "避免重大决定"]
        return v

    @validator('suggestions', 'risk_points')
    def truncate_list_items(cls, v):
        """Truncate individual list items if too long."""
        return [item[:100] + "..." if len(item) > 100 else item for item in v]