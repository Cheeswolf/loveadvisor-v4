from typing import Dict, Any, Optional, List
from pydantic import BaseModel, Field, validator


class AnalysisResult(BaseModel):
    """
    Main result model containing complete analysis output.

    This model represents the final output of the LoveAdvisor pipeline,
    including signals, strategies, and metadata.
    """
    relationship_stage: str = Field(..., description="Relationship stage: 初识期 / 暧昧期 / 拉扯期 / 冷淡期 / 无法判断")
    interest_level: str = Field(..., description="Interest level: 低 / 中 / 高 / 无法判断")
    psychological_analysis: str = Field(..., description="Psychological analysis based on signals")
    risk_points: List[str] = Field(..., description="Identified risk points in the relationship")
    suggestions: List[str] = Field(..., description="Actionable suggestions for the user")
    next_step: str = Field(..., description="Clear next step recommendation")
    debug: Optional[Dict[str, Any]] = Field(None, description="Debug information if debug mode enabled")

    @validator('relationship_stage')
    def validate_relationship_stage(cls, v):
        valid_values = {"初识期", "暧昧期", "拉扯期", "冷淡期", "无法判断"}
        if v not in valid_values:
            return "无法判断"
        return v

    @validator('interest_level')
    def validate_interest_level(cls, v):
        valid_values = {"低", "中", "高", "无法判断"}
        if v not in valid_values:
            return "无法判断"
        return v


